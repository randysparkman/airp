// Smoke test for the /api/email-profile route end-to-end.
// Inserts a fake assessment_completions row, generates a minimal PDF,
// POSTs through the live route (which calls Resend), then cleans up.
//
// Usage: node scripts/test-email-profile.mjs
// Requires: dev server running on :3000, SUPABASE_SERVICE_ROLE_KEY +
// NEXT_PUBLIC_SUPABASE_URL + RESEND_API_KEY in .env.local.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createClient } from "@supabase/supabase-js";
import { jsPDF } from "jspdf";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");

// Load .env.local
const envPath = path.join(repoRoot, ".env.local");
const envContent = fs.readFileSync(envPath, "utf-8");
for (const line of envContent.split("\n")) {
  const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
  if (m) process.env[m[1]] = m[2].replace(/^["']|["']$/g, "");
}

const TARGET_EMAIL = "rsparkman@gmail.com";
const ROUTE_URL = "http://localhost:3000/api/email-profile";

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!supabaseUrl || !serviceKey) {
  console.error("Missing Supabase env vars in .env.local");
  process.exit(1);
}

const sb = createClient(supabaseUrl, serviceKey);

// 1. Insert fake completion row
const testId = crypto.randomUUID();
console.log(`→ Inserting test row: ${testId}`);
const { error: insertError } = await sb.from("assessment_completions").insert({
  id: testId,
  role_profile: "medical-billing",
  respondent_name: "Randy Test",
  band: "Developing",
  orientation_level: "Developing",
  integration_level: "Developing",
  judgment_level: "Developing",
  assessment_data: { test: true, note: "Smoke test row — safe to delete" },
  completed_at: new Date().toISOString(),
});

if (insertError) {
  console.error("Insert failed:", insertError);
  process.exit(1);
}
console.log("  ✓ row inserted");

// 2. Generate minimal PDF
console.log("→ Generating placeholder PDF");
const doc = new jsPDF({ unit: "mm", format: "a4" });
doc.setFontSize(20);
doc.text("WorkPath Profile — Smoke Test", 20, 30);
doc.setFontSize(11);
doc.text("This is a placeholder PDF generated by the smoke test script.", 20, 45);
doc.text("If you are reading this in your inbox, the email loop works.", 20, 55);
doc.text(`Test row id: ${testId}`, 20, 70);
doc.text(`Sent: ${new Date().toISOString()}`, 20, 78);
const pdfBuffer = doc.output("arraybuffer");
const pdfBase64 = Buffer.from(pdfBuffer).toString("base64");
console.log(`  ✓ PDF generated (${Math.round(pdfBase64.length / 1024)} KB base64)`);

// 3. POST to the live route
console.log(`→ POST ${ROUTE_URL}`);
const res = await fetch(ROUTE_URL, {
  method: "POST",
  headers: { "Content-Type": "application/json" },
  body: JSON.stringify({
    completionId: testId,
    email: TARGET_EMAIL,
    pdfBase64,
    fileName: "workpath-smoke-test.pdf",
  }),
});

let data;
try {
  data = await res.json();
} catch {
  data = { error: "non-json response" };
}

console.log(`  ← status ${res.status}`);
console.log(`  ← body ${JSON.stringify(data, null, 2)}`);

// 4. Clean up
console.log(`→ Deleting test row ${testId}`);
const { error: deleteError } = await sb
  .from("assessment_completions")
  .delete()
  .eq("id", testId);
if (deleteError) {
  console.error("  ✗ delete failed:", deleteError);
} else {
  console.log("  ✓ row deleted");
}

// 5. Summary
console.log("");
if (data.sent === true) {
  console.log("✓ EMAIL SENT — check rsparkman@gmail.com inbox");
} else if (data.saved === true && data.sent === false) {
  console.log(`⚠ SAVED but NOT SENT — error: ${data.error || "unknown"}`);
} else {
  console.log(`✗ FAILED — error: ${data.error || "unknown"}`);
}
